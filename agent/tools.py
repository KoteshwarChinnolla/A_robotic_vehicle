class tools:

    def travel(self, A: str, B: str)->str:
        """ Travel from A to B

        Args :
        A: Starting point
        B: Destination

        """

        return f"Traveling from {A} to {B}"

    def speak(self,text:str)->str:
        """ Speak text

        Args :
        text: text to be spoken
        """
        return f"Speaking {text}"